struct MapView: UIViewRepresentable {
  func makeUIView(context: Context) -> MKMapView {
    var lat = 0.0
    var long = 0.0
    var pointCount = 0.0
    var maxLat = 0.0
    var minLat = 0.0
    var maxLong = 0.0
    var minLong = 0.0
    for point in points {
        lat += point[0]
        long += point[1]
        pointCount += 1
        minLat = point[0] < minLat ? point[0] : minLat
        maxLat = point[0] > maxLat ? point[0] : maxLat
        minLong = point[1] < minLong ? point[1] : minLong
        maxLong = point[1] > maxLong ? point[1] : maxLong
    }
    print(Double.pi)
    let avgLat = lat / pointCount
    let avgLong = long / pointCount
    
    let R = 6378.137
    let dLat = (maxLat * Double.pi / 180.0) - (minLat * Double.pi / 180.0)
    let dLon = 0.0
    let a = sin(dLat/2.0) * sin(dLat/2.0) + cos(maxLat * Double.pi / 180.0) * cos(minLat * Double.pi / 180.0) * sin(dLon/2.0) * sin(dLon/2.0)
    let c = 2.0 * atan2(a.squareRoot(), (1.0-a).squareRoot())
    let d = R * c

    let dLat2 = 0.0
    let dLon2 = (maxLong * Double.pi / 180.0) - (minLong * Double.pi / 180.0)
    let a2 = sin(dLat2/2.0) * sin(dLat2/2.0) + cos(avgLat * Double.pi / 180.0) * cos(avgLat * Double.pi / 180.0) * sin(dLon2/2.0) * sin(dLon2/2.0)
    let c2 = 2.0 * atan2(a2.squareRoot(), (1.0-a2).squareRoot())
    let d2 = R * c2
    
    let sourceCoordinate = CLLocationCoordinate2D(latitude: avgLat, longitude: avgLong)
    let region = MKCoordinateRegion(center: sourceCoordinate, latitudinalMeters: d + 3000, longitudinalMeters: d2 + 3000)

    mapView.region = region
    mapView.delegate = context.coordinator

    return mapView
  }

  func updateUIView(_ uiView: MKMapView, context: Context) {
  }

  // Acts as the MapView delegate
  class Coordinator: NSObject, MKMapViewDelegate {
    var parent: MapView

    init(_ parent: MapView) {
      self.parent = parent
    }

    func mapView(_ mapView: MKMapView, rendererFor overlay: MKOverlay) -> MKOverlayRenderer {
        let lineView = MKPolylineRenderer(overlay: overlay)
        lineView.strokeColor = .blue
        return lineView
    }
  }

  func makeCoordinator() -> Coordinator {
    Coordinator(self)
  }
}

